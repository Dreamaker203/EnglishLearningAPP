import { Course, Sentence } from "@/lib/types";
import { tokenizeSentence } from "@/lib/sentence";

function makeSentence(id: string, english: string, chinese: string): Sentence {
  return {
    id,
    english,
    chinese,
    words: tokenizeSentence(english),
  };
}

